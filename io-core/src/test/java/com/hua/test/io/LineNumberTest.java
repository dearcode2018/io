			lineNumberReader.mark(10);
			log.info("testLineNumberReader =====> " + lineNumberReader.read());
			
			// 读取一行数据，读取完成之后，行数自增1
			log.info("testLineNumberReader =====> " +lineNumberReader.readLine());
			
