	@Test
	public void testSequenceInputStream() {
		try {
			/*
			 以2个输入流顺序输出为例，多个同理
			 */
			InputStream firstInputStream = inputStream;
			InputStream secondInputStream = ClassPathUtil.getInputStream(BASIC_DIR + "小说_文本_01.txt");
			
			// 输出 顺序输入的结果
			outputStream = new FileOutputStream(ClassPathUtil.getClassPath(BASIC_DIR + "sequence-result.txt"));
			
			// 使用两个输入流进行顺序输出
			sequenceInputStream = new SequenceInputStream(firstInputStream, secondInputStream);
			
			while (Constant.NEGATIVE_ONE != (actualCount = sequenceInputStream.read(byteArray)))
			{
				outputStream.write(byteArray, 0, actualCount);
			}
			outputStream.flush();
			outputStream.close();
			inputStream.close();
			
		} catch (Exception e) {
			log.error("testSequenceInputStream =====> ", e);
		}
	}
	
